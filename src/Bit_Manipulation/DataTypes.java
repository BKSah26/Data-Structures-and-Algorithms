package Bit_Manipulation;

public class DataTypes {
    public static void main(String[] args) {
        byte x = 127;
        x+=1;
        System.out.println(x);
        x+=1;
        System.out.println(x);
        x+=1;
        System.out.println(x);

        int y = Integer.MAX_VALUE;
        System.out.println(y);
        y+=10;
        System.out.println(y);
    }
}
