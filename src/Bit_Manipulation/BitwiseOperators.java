package Bit_Manipulation;

public class BitwiseOperators {
    public static void main(String[] args) {
        System.out.println(51 & 42);
        System.out.println(51 | 42);
        System.out.println(51 ^ 42);

        int x = 5;
        System.out.println(~x);
        System.out.println(~x+1);
    }
}
